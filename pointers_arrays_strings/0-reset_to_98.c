/**
 * reset_to_98 - Updates the integer value pointed to by a pointer to 98
 * @n: A pointer to an integer that will be updated to 98
 *
 * Description: This function takes a pointer to an integer as a parameter
 * and sets the value it points to 98.
 */

void reset_to_98(int *n)
{
	*n = 98;
}
